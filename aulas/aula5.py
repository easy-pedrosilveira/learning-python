# Tipo de dado bool (boolean)
# Usado para mudar o fluxo do interpretador
# Ao questionar algo em um programa, existe duas resposta possíveis
# True == (sim) ou False == (não)
# existem vários operadores para "questionar".
# Dentre eles o ==, que é um operador lógico que questiona se um valor é igual ao outro

print(10 == 10) #Sim => True(Verdadeiro)
print(10 == 11) #Não =>  False(Falso)
print(type(True))
print(type(False))
print(type(10 == 11))
print(type(10 == 11))